import { existsSync, mkdirSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const exhibitionsRoot = path.join(repoRoot, "public", "exhibitions");
const outputPath = path.join(repoRoot, "src", "lib", "generated", "exhibition-media-manifest.ts");

const imageExtension = /\.(jpe?g|png|webp|gif)$/i;
const sourceImageExtension = /\.(jpe?g|png)$/i;

function imageSize(filePath) {
  const data = readFileSync(filePath);

  if (data.toString("ascii", 1, 4) === "PNG") {
    return { width: data.readUInt32BE(16), height: data.readUInt32BE(20) };
  }

  if (data[0] !== 0xff || data[1] !== 0xd8) return undefined;

  let offset = 2;
  while (offset < data.length) {
    if (data[offset] !== 0xff) {
      offset += 1;
      continue;
    }

    const marker = data[offset + 1];
    if (marker >= 0xc0 && marker <= 0xc3) {
      return {
        height: data.readUInt16BE(offset + 5),
        width: data.readUInt16BE(offset + 7),
      };
    }

    const length = data.readUInt16BE(offset + 2);
    offset += length + 2;
  }

  return undefined;
}

function hasSourceImageSibling(sectionPath, fileName) {
  if (!fileName.toLowerCase().endsWith(".webp")) return false;

  const baseName = fileName.replace(/\.webp$/i, "");

  return [".jpg", ".jpeg", ".png"].some((extension) =>
    existsSync(path.join(sectionPath, `${baseName}${extension}`))
  );
}

function optimizedImageName(sectionPath, fileName) {
  if (!sourceImageExtension.test(fileName)) return fileName;

  const webpName = fileName.replace(sourceImageExtension, ".webp");

  return existsSync(path.join(sectionPath, webpName)) ? webpName : fileName;
}

function sectionImages(folder, section) {
  const sectionPath = path.join(exhibitionsRoot, folder, section);

  return readdirSync(sectionPath, { withFileTypes: true })
    .filter((file) => file.isFile() && imageExtension.test(file.name))
    .filter((file) => !hasSourceImageSibling(sectionPath, file.name))
    .sort((first, second) => first.name.localeCompare(second.name, undefined, { numeric: true }))
    .map((file) => {
      const size = imageSize(path.join(sectionPath, file.name));

      return {
        name: optimizedImageName(sectionPath, file.name),
        isWide: size ? size.width / size.height > 1.15 : false,
      };
    });
}

const manifest = {};

for (const folder of readdirSync(exhibitionsRoot, { withFileTypes: true })) {
  if (!folder.isDirectory()) continue;

  const folderPath = path.join(exhibitionsRoot, folder.name);
  const sections = {};

  for (const section of readdirSync(folderPath, { withFileTypes: true })) {
    if (!section.isDirectory()) continue;

    sections[section.name.toLowerCase()] = {
      name: section.name,
      images: sectionImages(folder.name, section.name),
    };
  }

  manifest[folder.name] = { sections };
}

mkdirSync(path.dirname(outputPath), { recursive: true });
writeFileSync(
  outputPath,
  `// This file is generated by scripts/generate-exhibition-media-manifest.mjs.\n` +
    `// Do not edit it manually.\n\n` +
    `export const EXHIBITION_MEDIA_MANIFEST = ${JSON.stringify(manifest, null, 2)} as const;\n`,
  "utf8"
);

